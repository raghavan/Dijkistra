package jsonmaker;

public class NodeDetail {
	String nodeFrom;
	String nodeTo;
	public String getNodeFrom() {
		return nodeFrom;
	}
	public void setNodeFrom(String nodeFrom) {
		this.nodeFrom = nodeFrom;
	}
	public String getNodeTo() {
		return nodeTo;
	}
	public void setNodeTo(String nodeTo) {
		this.nodeTo = nodeTo;
	}
	
	
}
